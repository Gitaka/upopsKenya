		<h2 style="padding-bottom:1%;color:#006400;">FAQ</h2>
		<h4 style="padding-bottom:1%;">What are U-POPS?</h4>
		<p>
			Unintentionally formed Persistent Organic Pollutants (U-POPs) are defined in the Stockholm Convention as polychlorinated dibenzo-p-dioxins (PCDDs) and polychlorinated dibenzofurans (PCDF). These are as organic compounds of natural and anthropogenic origin that resist photolytic, chemical and biological degradation.  POPs are unintentionally formed and released as by-products from thermal processes involving organic matter and chlorine reactions. They may also be produced through non-anthropogenic activities. Observations show that U-POPs may have been present in the environment for considerably longer than the onset of industrial activity. Studies at Lancaster University (Alcock et al, 1998) and elsewhere (Ferrario et al 1998; Winter et al 1998) have detected the presence of these substances in environmental samples collected and stored from late 1800.
		</p>
		<h4 style="padding-bottom:1%;">What are UPOPS Characteristics?</h4>
		<p>
			The U-POPs are toxic chemicals that are persistent and bioaccummulative and are capable of being globally transported.  Persistent materials do not easily degrade in the environment. Bio accumulative materials are usually fat-soluble and build up in higher trophic levels, including humans.  Because U-POPs are semi-volatile compounds, they can evaporate and condense in the environment thus making them geographically mobile.  Their transport results in widespread distribution across the earth, including regions where they have never been produced hence appear in environmental samples in these areas.
		</p>
		<h4 style="padding-bottom:1%;">What risks do UPOPS have on human beings?</h4>
		<p>
			Because these chemicals exist throughout the environment, almost every living creature including man has been exposed to U-POPs.  Exposure arises mainly through consumption of fatty foods, including breast milk, but can also arise from accidental or occupational situations.  Their health effects depend on a variety of factors including the level of exposure, concentration, the age at which exposure occurs, and how long and how often the exposure occurs (frequency).
		</p>
		<p>
			Most of the information on the toxicity of these chemicals is based on the extensive studies with experimental animals using the most toxic of the PCDD and PCDF compounds. The International Agency for Research on Cancer (IARC) under the World Health Organization (WHO) has identified tetrachlorodibenzodioxin (TCDD) with the chlorines in positions 2,3,7,8 of the dioxin skeleton structure as the most toxic.
		</p>
		<h4 style="padding-bottom:1%;">Are UPOPS used commercially?</h4>
		<p>
			There is no known value, commercial or otherwise of PCDD and PCDF, hence they are not intentionally manufactured for any reason other than laboratory purposes. They are generated as by-products during the manufacture of certain industrial products like paper, herbicides and textiles and also in processes that involve heating or burning in the presence of chlorine-containing substances or chlorine as a contaminant.
		</p>
		<h4 style="padding-bottom:1%;">What is the tolerable daily intake for UPOPS?</h4>
		<p>
			The Tolerable Daily Intake (TDI) is the amount of intake per kg of body weight per day of a chemical substance judged not to give rise to manifestations of health effects if such an amount is taken every day for an entire lifetime.  The TDI established by the World Health Organization in 1998 for chemicals TCDD was set at 1-4pg/kg body weight/day.  In 2001, the Joint FAO/WHO Expert Committee on Food Additives (JECFA) set a provisional TCDD TDI of 70-pg/kg body weight/month, which approximates to 2.3 pg/kg body weight/day.
		</p>
		<p>
			Prolonged exposure to TCDD results in a wide variety of reproductive and developmental effects in a number of species of experimental animals including reduced viability, structural alternation, growth retardation, and functional alterations.  There is also evidence of neurobehavioral effects and effects on immune and various endocrine functions including those of the thyroid.  Because of this evidence in animals particularly at high doses (although in some cases at doses close to those with relevance for human beings), scientists are concerned about the potential for these same effects to occur in humans, especially the effects of prenatal exposure to developing children.
		</p>
		<p>
			The U-POPs also enter the body through breathing contaminated air especially for those people working or living near sources like incinerators and metal smelters. Entry into the body may also occur through drinking contaminated water or eating contaminated foods such as beef, pork, poultry, fish or dairy products, especially if these organisms had been exposed to the contaminants. Eating of contaminated food has been found to be the major contributor to exposure, accounting for 90%. This is because the contaminants build up in the fatty tissues of the exposed organisms. Other avenues of exposure such as burning of household waste and forest fires contribute a relatively small fraction of the total exposure. The US EPA has set the exposure limit of 0.00003g of TCDD per litre of drinking water, while the Food and Drug Administration of U.S.A recommends not eating fish with more than 50 parts per trillion of TCDD.
		</p>
