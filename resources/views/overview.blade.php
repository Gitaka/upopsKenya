         <div class="row">
       	  <div class="col-sm-3 project-title"><h2 style="color:#006400;">
       	  Brief Description.</h2>
       	  </div>
       	  <div class="col-sm-9 project-title-body">
       	  	<p>
       	  		The project intend to protect human health and the environment by managing the risks posed by production , use, import and export of chemicals and  reducing / preventing the release of U-POPs and toxic compounds originating from the unsafe management of waste in two key sectors: Health Care Waste and Municipal Waste. These sectors are among the highest priorities identified in the reviewed and updated NIP. On the HCWM waste side, the project will adopt an integrated approach aimed at increasing the proper management of waste within the hospital facilities (increasing segregation, reducing waste generation) and by replacing the dangerous disposal waste modality currently adopted (open burning or burning in single chamber incinerators) by SC-compliant equipment. Training will be deliver both at HCF level and in classroom training events, and will be based on the WHO bluebook guidance tailored to the country needs.  On the municipal waste side, the project intends to reinforce the 3R (Reduce, Reuse, Recycle) economy on two specific waste streams, by enhancing their upstream collection, ensuring the quality of recovered material, and securing access to national market by promoting cooperation with domestic industries. This for providing a valid alternative to the dumpsite economy, and preventing the release in the environment of U-POPs and toxic substance release upon open burning of these waste streams. The project also includes a component related to the sound management of chemicals, by implementing activities on U-POPs monitoring, upgrading of the relevant regulation on chemicals, and establishing a PRTR database.
       	  	</p>
