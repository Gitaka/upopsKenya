		<h2 style="padding-bottom:1%;color:#006400;">Rotterdam Convention</h2>
        <h3>Overview</h3>
        <p style="font-weight:bolder;">The text of the Rotterdam Convention was adopted on 10 September 1998 by a Conference of Plenipotentiaries in Rotterdam, the Netherlands. The Convention entered into force on 24 February 2004.</p>
        <h3>Objectives of the convention</h3>
        <ul>
        	<li>1) to promote shared responsibility and cooperative efforts among Parties in the international trade of certain hazardous chemicals in order to protect human health and the environment from potential harm;</li>
        	<li>2) to contribute to the environmentally sound use of those hazardous chemicals, by facilitating information exchange about their characteristics, by providing for a national decision-making process on their import and export and by disseminating these decisions to Parties.</li>
        </ul>
        <p>The Convention creates legally binding obligations for the implementation of the Prior Informed Consent (PIC) procedure. It built on the voluntary PIC procedure, initiated by UNEP and FAO in 1989 and ceased on 24 February 2006.</p>
        <h3>Major Provisions</h3>
        <p>The Convention covers pesticides and industrial chemicals that have been banned or severely restricted for health or environmental reasons by Parties and which have been notified by Parties for inclusion in the PIC procedure.  One notification from each of two specified regions triggers consideration of addition of a chemical to Annex III of the Convention. Severely hazardous pesticide formulations that present a risk under conditions of use in developing countries or countries with economies in transition may also be proposed for inclusion in Annex III.</p>
        <p>Once a chemical is included in Annex III, a "decision guidance document" (DGD) containing information concerning the chemical and the regulatory decisions to ban or severely restrict the chemical for health or environmental reasons, is circulated to all Parties. </p>
        <p>
        	Parties have nine months to prepare a response concerning the future import of the chemical. The response can consist of either a final decision (to allow import of the chemical, not to allow import, or to allow import subject to specified conditions) or an interim response. Decisions by an importing country must be trade neutral (that is, decisions must apply equally to domestic production for domestic use as well as to imports from any source).
        </p>
        <p>
        	The import decisions are circulated and exporting country Parties are obligated under the Convention to take appropriate measure to ensure that exporters within its jurisdiction comply with the decisions.
        </p>
        <p>The Convention promotes the exchange of information on a very broad range of chemicals. It does so through:</p>
        <ul>
        	<li>1) the requirement for a Party to inform other Parties of each national ban or severe restriction of a chemical;</li>
        	<li>2) the possibility for Party which is a developing country or a country in transition to inform other Parties that it is experiencing problems caused by a severely hazardous pesticide formulation under conditions of use in its territory;</li>
        	<li>
        		3) the requirement for a Party that plans to export a chemical that is banned or severely restricted for use within its territory, to inform the importing Party that such export will take place, before the first shipment and annually thereafter;
        	</li>
        	<li>
        		4) the requirement for an exporting Party, when exporting chemicals that are to be used for occupational purposes, to ensure that an up-to-date safety data sheet is sent to the importer; and
        	</li>
        	<li>
        		5) labeling requirements for exports of chemicals included in the PIC procedure, as well as for other chemicals that are banned or severely restricted in the exporting country.
        	</li>
        </ul>
